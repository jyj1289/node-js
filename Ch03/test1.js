// 자바스크립트는 자료형을 명시하지 않는 언어
// 내부에서는 자료형에 따라 변수 상자의 크기가 달라짐

// 자료형
// - boolean : true와 false의 두 가지 값을 가지는 자료형
// - Number : 64비트 형식의 IEEE 754 값이며 정수나 부동소수 값을 가지는 자료형
//   몇 가지 상징적인 값을 가질 수 있음 NaN(숫자가 아님), 
//   무한대(Numebr.MAX_VALUE로 확인), 무한대(Number.MIN_VALUE로 확인)
// - String : 문자열 값을 가지는 자료형
// - undefined : 값을 할당하지 않은 변수의 값
// - null : 존재하지 않는 값을 가리키는 값
// - Object : 객체를 값으로 가지는 자료형,
//   객체는 속성들을 담고 있는 가방(Collection)으로 볼 수 있으며,
//   대표적인 객체로 Array나 Date를 들 수 있음

var name;
console.log('name : ' + name)       // 값이 비어있기 때문에 undefined 가 나온다

var age = 20;
console.log('age : ' + age)

name = '홍길동'
console.log('name : ' + name)







